from cmath import sqrt
print('Seja Bem-vindo')
num = float(input('Digite um número: '))
print()
dob = num*2
trp = num*3
rq = sqrt(num)
print(f' O número digitado foi {num}. \n O dobro do número é = {dob}. \n O triplo do número é = {trp}.')
print(f'A raíz quadrada do número é = {rq:.2f}.')
print('Fim do código')
